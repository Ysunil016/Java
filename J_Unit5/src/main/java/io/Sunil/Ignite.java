package io.Sunil;

public class Ignite {

	protected int add(int a, int b) {
		return a + b;
	}

	protected int mul(int a, int b) {
		return a * b;
	}

	protected int sub(int a, int b) {
		return a - b;
	}

	protected int div(int a, int b) {
		return a / b;
	}

	protected double calculateAreaCircle(int radius) {
		return Math.PI * radius * radius;
	}

}
